public class GetandSetValues {

    private String name;
    
    private int age;

    public int getAge()
    {
        return age;
    }

    public void setAge(int ageInput)
    {
        this.age = ageInput;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String nameInput)
    {
        this.name = nameInput;
    }
}
